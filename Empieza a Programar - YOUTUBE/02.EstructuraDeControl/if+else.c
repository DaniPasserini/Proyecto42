//Estructuras selectivas + else

#include <stdio.h>

int main(){
	int x;
	x = 4;
if (x==5){
	printf ("El valor de x es 5.\n");
} //si la condicion no se cumple entonces no mostraria la nota.

else { // Si no cumple la condicion mostrara el siguiente cartel.
	printf ("El valor de x es distinto de 5.\n");
}
return 0;}
